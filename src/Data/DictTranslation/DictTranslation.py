    def transaltionSequense(self, sequense, start, end):
        stopCodon = (-1, -1)
        seqAminoacid = ""
        for i in range(start, end + 1, 3):
            codon = sequense[i] + sequense[i + 1] + sequense[i + 2]
            aminoacid = self.getAminoacid(codon)
            seqAminoacid += aminoacid
            if aminoacid == "*": 
                stopCodon = (i, i + 2)
                break
        return (seqAminoacid, stopCodon)

    def getAminoacid(self, codon):
        return self.translation[codon]